// ── Fast sine (radians input, float output) ─────────────────────────
// Drop-in replacement for sinf(). 256-step resolution.

static inline float fastSin(float radians) {
    // 256 entries per 2π → multiply by 256/(2π) ≈ 40.7436
    int32_t idx = (int32_t)(radians * 40.7436654f);
    return sinLUT[idx & 0xFF] * (1.0f / 32767.0f);
}

// ── Fast sine (0–1 phase input, float output) ───────────────────────
// For breathing/oscillator patterns where phase is already 0–1.

static inline float fastSinPhase(float phase01) {
    uint8_t idx = (uint8_t)(phase01 * 256.0f);
    return sinLUT[idx] * (1.0f / 32767.0f);
}

// ── Fast gamma 2.4 (float 0–1 input, float 0–1 output) ─────────────
// Drop-in replacement for powf(x, 2.4).

static inline float fastGamma24(float x) {
    if (x <= 0.0f) return 0.0f;
    int32_t idx = (int32_t)(x * 255.0f);
    if (idx > 255) idx = 255;
    return gammaLUT[idx] * (1.0f / 65535.0f);
}

// ── Fast exponential decay ──────────────────────────────────────────
// Approximates powf(base, exponent) for base near 1.0.
// Linear approximation: 1 + exp*(base-1). Error < 0.1% for
// base in [0.9, 1.0] and exponent in [0, 2].

static inline float fastDecay(float base, float exponent) {
    return 1.0f + exponent * (base - 1.0f);
}
