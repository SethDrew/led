static uint32_t lerpPalette(const RGB *pal, uint8_t count, float t) {
    // t in [0, 1] -> interpolate across color stops
    float idx = t * (count - 1);
    uint8_t lo = (uint8_t)idx;
    uint8_t hi = lo + 1;
    if (hi >= count) hi = count - 1;
    float frac = idx - lo;
    uint8_t r = pal[lo].r + (pal[hi].r - pal[lo].r) * frac;
    uint8_t g = pal[lo].g + (pal[hi].g - pal[lo].g) * frac;
    uint8_t b = pal[lo].b + (pal[hi].b - pal[lo].b) * frac;
    return ((uint32_t)r << 16) | ((uint32_t)g << 8) | b;
}

void renderGradient(Adafruit_NeoPixel &strip, const EffectState &state) {
    uint16_t n = strip.numPixels();
    const RGB *pal;
    uint8_t count;
    switch (state.palette) {
        case OKLCH_RAINBOW:     pal = oklchRainbow;     count = oklchRainbowCount;     break;
        case BLUE_PURPLE:       pal = bluePurple;       count = bluePurpleCount;       break;
        case LAVA:              pal = lava;             count = lavaCount;             break;
        case OCEAN_BREEZE:      pal = oceanBreeze;      count = oceanBreezeCount;      break;
        case SUNSET_REAL:       pal = sunsetReal;       count = sunsetRealCount;       break;
        case FIRE:              pal = fire;             count = fireCount;             break;
        case MAGENTA_EVENING:   pal = magentaEvening;   count = magentaEveningCount;   break;
        case CORAL_REEF:        pal = coralReef;        count = coralReefCount;        break;
        case EMERALD_DRAGON:    pal = emeraldDragon;    count = emeraldDragonCount;    break;
        default:                pal = sapFlow;          count = sapFlowCount;          break;
    }
    for (uint16_t i = 0; i < n; i++) {
        float t = (float)i / (n - 1);
        strip.setPixelColor(i, lerpPalette(pal, count, t));
    }
}
